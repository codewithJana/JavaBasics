package Assignment_Exercises;

public class Q4_Multiplication_Table_with_Formatted_Output {

    public static void main(String[] args) {
        int n = 7;

        // Loop from 1 to 12 to generate the table
        for (int i = 1; i <= 12; i++) {
            System.out.println(n + " x " + i + " = " + (n * i));
        }
    }


}
